# #2.变量：变量用来存储值。
# #2.1变量命名规则：
# # 变量名只能包含字母、数字和下划线。变量名可以字母或下划线打头，但不能以数字打头，如：message_1。
# # 变量名不能包含空格，但可使用下划线来分隔其中的单词。如：greeting_message。
# # 不要将Python关键字和函数名用作变量名。
# # 变量名应既简短又具有描述性。如：name比n好，student_name比s_n。
# # 慎用小写字母l和大写字母O，因为它们可能被人错看成数字1和0。

